    private void buttonUpdateActionPerformed(java.awt.event.ActionEvent evt) {
        var title = fieldTitlle1.getText();
        var ISB = labelOfISB1.getText();
        var stockSrt = fieldStock1.getText();

        int stock;
        try {
            stock = Integer.parseInt(stockSrt);
        } catch (NumberFormatException e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Invalid stock number.");
            return;
        }

        if (selectBook == null) {
            javax.swing.JOptionPane.showMessageDialog(this, "No book selected for update.");
            return;
        }
        System.out.println("Selected Book ISBN: " + ISB);
        System.out.println("Selected Book Title: " + selectBook.getTitle());
        System.out.println("Selected Book Publisher: " + selectBook.getPublisher().getName());
        selectBook.setStock(stock);
        selectBook.setTitle(title);

        try {
            bookUpdate.updateBook(selectBook);
            javax.swing.JOptionPane.showMessageDialog(this, "Book update succesfully");
            dispose();
        } catch (Exception e) {
            javax.swing.JOptionPane.showConfirmDialog(this, "Error updating book: " + e.getMessage());
        }

    }

